package com.black.web.bean;

public class LinkedinFollowEntity {

	private String name;	//关注用户名称
	private String count;	//关注用户被关注数量
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	
}
