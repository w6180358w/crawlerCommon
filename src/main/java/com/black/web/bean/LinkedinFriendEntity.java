package com.black.web.bean;

public class LinkedinFriendEntity {

	private String name;		//好友姓名
	private String icon;		//好友头像
	private String detail;		//好友职业头衔
	private String address;		//好友地址
	private String intimacy;	//亲密度
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getIntimacy() {
		return intimacy;
	}
	public void setIntimacy(String intimacy) {
		this.intimacy = intimacy;
	}
	
}
